int oled_display_distance(u32 distance)
{
	char buf[20];

	if (!oled_client)
		return -ENODEV;

	mutex_lock(&oled_lock);

	oled_clear();
	snprintf(buf, sizeof(buf), "Distance:");
	SH1106_WriteString(0, 0, buf);

	snprintf(buf, sizeof(buf), "%u cm", distance);
	SH1106_WriteString(3, 0, buf);

	mutex_unlock(&oled_lock);
	return 0;
}

EXPORT_SYMBOL_GPL(oled_display_distance);

/* oled probe */
static int oled_probe(struct i2c_client *client)
{
	oled_client = client;

	oled_display_init();
	oled_clear();

	SH1106_WriteString(0, 0, "Press Button");
	SH1106_WriteString(3, 0, "to measure :-)");

	dev_info(&client->dev, "OLED (DT) probed\n");
	return 0;
}

static void oled_remove(struct i2c_client *client)
{
	oled_clear();
	oled_client = NULL;
}

/* DT matching */
static const struct of_device_id oled_of_match[] = {
	{ .compatible = "custom,sh1106-oled" },
	{ }
};
MODULE_DEVICE_TABLE(of, oled_of_match);

static struct i2c_driver oled_driver = {
	.probe_new = oled_probe,
	.remove = oled_remove,
	.driver = {
		.name = "distance_oled",
		.of_match_table = oled_of_match,
	},
};

/* character driver will call init and exit */
int oled_driver_init(struct distance_logger_dev *dl_dev)
{
	return i2c_add_driver(&oled_driver);
}

void oled_driver_exit(void)
{
	i2c_del_driver(&oled_driver);
}
