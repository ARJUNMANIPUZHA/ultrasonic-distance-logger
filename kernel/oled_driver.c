/* Global variable */
static struct i2c_client *oled_client;
static DEFINE_MUTEX(oled_lock);

/* i2c write */
static int oled_i2c_write(u8 *buf, u32 len)
{
	if (!oled_client)
		return -ENODEV;

	return i2c_master_send(oled_client, buf, len);
}

/* SH1106 write */
static void SH1106_Write(bool is_cmd, u8 data)
{
	u8 buf[2];

	buf[0] = is_cmd ? 0x00 : 0x40;
	buf[1] = data;

	oled_i2c_write(buf, 2);
}

/* oled init */
static void oled_display_init(void)
{
	msleep(100);

	SH1106_Write(true, 0xAE);
	SH1106_Write(true, 0xD5); SH1106_Write(true, 0x80);
	SH1106_Write(true, 0xA8); SH1106_Write(true, 0x3F);
	SH1106_Write(true, 0xD3); SH1106_Write(true, 0x00);
	SH1106_Write(true, 0x40);
	SH1106_Write(true, 0xA1);
	SH1106_Write(true, 0xC8);
	SH1106_Write(true, 0xDA); SH1106_Write(true, 0x12);
	SH1106_Write(true, 0x81); SH1106_Write(true, 0x80);
	SH1106_Write(true, 0xA4);
	SH1106_Write(true, 0xA6);
	SH1106_Write(true, 0xAF);
}

static void oled_clear(void)
{
	u8 page, col;

	for (page = 0; page < 8; page++) {
		SH1106_Write(true, 0xB0 + page);
		SH1106_Write(true, 0x00);
		SH1106_Write(true, 0x10);

		for (col = 0; col < 128; col++)
			SH1106_Write(false, 0x00);
	}
}

