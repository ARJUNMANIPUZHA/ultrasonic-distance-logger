/* character render */
/*
 * Font format:
 *  - 10 rows per character
 *  - Each row is a 16-bit value
 *  - Only upper 7 bits are used
 *
 * SH1106:
 *  - Page = 8 vertical pixels
 *  - Character spans 2 pages
 */

static void SH1106_WriteChar(uint8_t page, uint8_t col, char ch)
{
    uint8_t i;
    uint8_t c = ch - 0x20;
    uint8_t column = 2 + col;
    uint8_t upper, lower;
    uint16_t row_bits;

    if (c >= 95)
        return;

    /* -------- PAGE 0 (rows 0–7) -------- */
    SH1106_Write(true, 0xB0 + page);
    SH1106_Write(true, 0x00 | (column & 0x0F));
    SH1106_Write(true, 0x10 | (column >> 4));

    for (i = 0; i < 7; i++) {
        upper = 0;
        for (int r = 0; r < 8; r++) {
            row_bits = Font7x10[c * 10 + r];
            if (row_bits & (0x4000 >> i))
                upper |= (1 << r);
        }
        SH1106_Write(false, upper);
    }
    SH1106_Write(false, 0x00);

    /* -------- PAGE 1 (rows 8–9) -------- */
    SH1106_Write(true, 0xB0 + page + 1);
    SH1106_Write(true, 0x00 | (column & 0x0F));
    SH1106_Write(true, 0x10 | (column >> 4));

    for (i = 0; i < 7; i++) {
        lower = 0;
        for (int r = 8; r < 10; r++) 
		{
            row_bits = Font7x10[c * 10 + r];
            if (row_bits & (0x4000 >> i))
                lower |= (1 << (r - 8));
        }
        SH1106_Write(false, lower);
    }
    SH1106_Write(false, 0x00);
}

static void SH1106_WriteString(uint8_t page, uint8_t col, const char *str)
{
    while (*str) 
	{
        SH1106_WriteChar(page, col, *str++);
        col += 8;   // 7 pixels + spacing
    }
}

